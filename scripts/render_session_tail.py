def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat().replace("+00:00", "Z")


def _load_field_map() -> dict[str, str]:
    map_file = os.environ.get("FIELD_MAP_FILE", "")
    if map_file and Path(map_file).exists():
        with open(map_file) as f:
            return json.load(f)
    # Try sibling field_map.json
    sibling = Path(__file__).parent / "field_map.json"
    if sibling.exists():
        with open(sibling) as f:
            return json.load(f)
    return DEFAULT_FIELD_MAP


def _extract_nested(data: dict, dotted_key: str) -> Any:
    """Extract a value from nested dict using dotted key notation."""
    parts = dotted_key.split(".")
    current = data
    for part in parts:
        if isinstance(current, dict):
            current = current.get(part)
        else:
            return None
    return current


def _map_record(record: dict[str, Any], field_map: dict[str, str]) -> dict[str, Any]:
    """Map a raw JSONL record to the observability schema."""
    doc: dict[str, Any] = {{
        "@timestamp": _now_iso(),
        "event.kind": "event",
        "event.category": "process",
        "service.name": SERVICE_NAME,
        "observer.ingest_path": "session_tail",
        "observer.type": "file_tail",
    }}

    # Try to extract timestamp from the record
    for ts_key in ("timestamp", "created_at", "time", "@timestamp", "ts"):
        ts_val = record.get(ts_key) or _extract_nested(record, ts_key)
        if ts_val:
            doc["@timestamp"] = str(ts_val)
            break

    # Apply field mapping
    for src_key, dst_key in field_map.items():
        value = record.get(src_key)
        if value is None:
            # Try dotted notation extraction
            value = _extract_nested(record, src_key)
        if value is None:
            continue
        if dst_key in doc:
            continue  # Don't overwrite already-set values

        # Special handling for boolean success → event.outcome
        if dst_key == "event.outcome" and isinstance(value, bool):
            doc[dst_key] = "success" if value else "failure"
        elif dst_key == "event.outcome" and isinstance(value, str):
            low = value.lower()
            if low in ("success", "ok", "pass", "true"):
                doc[dst_key] = "success"
            elif low in ("failure", "fail", "error", "false"):
                doc[dst_key] = "failure"
            else:
                doc[dst_key] = value
        else:
            doc[dst_key] = value

    # Auto-derive component_type from role
    role = record.get("role") or record.get("type") or ""
    if isinstance(role, str):
        role_lower = role.lower()
        if role_lower in ("assistant", "model", "llm"):
            doc.setdefault("gen_ai.agent_ext.component_type", "llm")
            doc.setdefault("gen_ai.operation.name", "chat")
        elif role_lower in ("tool", "function", "tool_result"):
            doc.setdefault("gen_ai.agent_ext.component_type", "tool")
            doc.setdefault("gen_ai.operation.name", "tool_call")
        elif role_lower in ("system", "user"):
            doc.setdefault("gen_ai.agent_ext.component_type", "runtime")

    # Extract usage from nested objects (common in OpenAI-style responses)
    usage = record.get("usage")
    if isinstance(usage, dict):
        if usage.get("prompt_tokens") is not None:
            doc.setdefault("gen_ai.usage.input_tokens", usage["prompt_tokens"])
        if usage.get("input_tokens") is not None:
            doc.setdefault("gen_ai.usage.input_tokens", usage["input_tokens"])
        if usage.get("completion_tokens") is not None:
            doc.setdefault("gen_ai.usage.output_tokens", usage["completion_tokens"])
        if usage.get("output_tokens") is not None:
            doc.setdefault("gen_ai.usage.output_tokens", usage["output_tokens"])
        if usage.get("total_tokens") is not None:
            doc.setdefault("gen_ai.usage.total_tokens", usage["total_tokens"])

    # Fallback: set event.outcome if not yet determined
    if "event.outcome" not in doc:
        doc["event.outcome"] = "failure" if doc.get("error.type") else "success"

    # Include raw content as message (truncated)
    content = record.get("content") or record.get("text") or record.get("message")
    if isinstance(content, str) and content:
        doc["message"] = content[:500]

    return doc


def _build_otlp_log(document: dict[str, Any]) -> dict[str, Any]:
    """Wrap a flat document into an OTLP/HTTP JSON log payload."""
    now_ns = str(int(time.time() * 1_000_000_000))
    attributes = []
    body_text = document.pop("message", "session_tail event")
    for key, value in document.items():
        if key == "@timestamp":
            continue
        if isinstance(value, bool):
            attributes.append({{"key": key, "value": {{"boolValue": value}}}})
        elif isinstance(value, int):
            attributes.append({{"key": key, "value": {{"intValue": str(value)}}}})
        elif isinstance(value, float):
            attributes.append({{"key": key, "value": {{"doubleValue": value}}}})
        elif value is not None:
            attributes.append({{"key": key, "value": {{"stringValue": str(value)}}}})
    return {{
        "resourceLogs": [{{
            "resource": {{
                "attributes": [
                    {{"key": "service.name", "value": {{"stringValue": SERVICE_NAME}}}},
                    {{"key": "observer.product", "value": {{"stringValue": "elasticsearch-agent-observability"}}}},
                ]
            }},
            "scopeLogs": [{{
                "scope": {{"name": "session-tail"}},
                "logRecords": [{{
                    "timeUnixNano": now_ns,
                    "observedTimeUnixNano": now_ns,
                    "severityNumber": 9,
                    "severityText": "INFO",
                    "body": {{"stringValue": body_text}},
                    "attributes": attributes,
                }}],
            }}],
        }}]
    }}


def _post_to_bridge(payload: dict[str, Any], bridge_url: str) -> bool:
    """POST an OTLP log payload to the bridge. Returns True on success."""
    endpoint = f"{{bridge_url.rstrip('/')}}/v1/logs"
    data = json.dumps(payload, ensure_ascii=False).encode("utf-8")
    req = request.Request(endpoint, data=data, method="POST")
    req.add_header("Content-Type", "application/json")
    try:
        with request.urlopen(req, timeout=10) as resp:
            return resp.status in (200, 201)
    except (error.HTTPError, error.URLError, OSError) as exc:
        print(f"[session-tail] bridge POST failed: {{exc}}", file=sys.stderr)
        return False


class SessionTailer:
    """Watches JSONL files in a directory, tailing new lines and emitting to bridge."""

    def __init__(self, session_dir: str, pattern: str, field_map: dict[str, str], bridge_url: str):
        self.session_dir = session_dir
        self.pattern = pattern
        self.field_map = field_map
        self.bridge_url = bridge_url
        self._offsets: dict[str, int] = {{}}  # file path → last read position
        self._emitted = 0
        self._errors = 0

    def _discover_files(self) -> list[str]:
        full_pattern = os.path.join(self.session_dir, self.pattern)
        return sorted(glob.glob(full_pattern, recursive=True))

    def _tail_file(self, filepath: str) -> list[dict[str, Any]]:
        records = []
        try:
            size = os.path.getsize(filepath)
        except OSError:
            return records

        last_pos = self._offsets.get(filepath, 0)
        if size < last_pos:
            # File was truncated / rotated — re-read from start
            last_pos = 0
        if size == last_pos:
            return records

        try:
            with open(filepath, "r", encoding="utf-8", errors="replace") as f:
                f.seek(last_pos)
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    try:
                        record = json.loads(line)
                        if isinstance(record, dict):
                            records.append(record)
                    except json.JSONDecodeError:
                        pass  # Skip malformed lines
                self._offsets[filepath] = f.tell()
        except OSError:
            pass
        return records

    def poll_once(self) -> int:
        """Poll all matching files for new records. Returns count of emitted events."""
        count = 0
        for filepath in self._discover_files():
            records = self._tail_file(filepath)
            for record in records:
                doc = _map_record(record, self.field_map)
                # Try to infer session_id from filename if not in the record
                if "gen_ai.conversation.id" not in doc:
                    fname = Path(filepath).stem
                    doc["gen_ai.conversation.id"] = fname
                payload = _build_otlp_log(doc)
                if _post_to_bridge(payload, self.bridge_url):
                    count += 1
                    self._emitted += 1
                else:
                    self._errors += 1
        return count

    def run_forever(self) -> None:
        print(f"[session-tail] Watching {{self.session_dir}}/{{self.pattern}} → {{self.bridge_url}}")
        print(f"[session-tail] Poll interval: {{POLL_INTERVAL}}s")
        try:
            while True:
                n = self.poll_once()
                if n > 0:
                    print(f"[session-tail] Emitted {{n}} events (total: {{self._emitted}}, errors: {{self._errors}})")
                time.sleep(POLL_INTERVAL)
        except KeyboardInterrupt:
            print(f"\\n[session-tail] Stopped. Total emitted: {{self._emitted}}, errors: {{self._errors}}")


def main() -> int:
    parser = argparse.ArgumentParser(description="Tail session JSONL files and emit to OTLP bridge")
    parser.add_argument("--session-dir", default=SESSION_DIR, help="Directory containing JSONL session files")
    parser.add_argument("--pattern", default=SESSION_GLOB, help="Glob pattern for session files")
    parser.add_argument("--bridge-url", default=BRIDGE_URL, help="OTLP HTTP bridge URL")
    parser.add_argument("--once", action="store_true", help="Run once and exit (don't tail)")
    args = parser.parse_args()

    field_map = _load_field_map()
    bridge = args.bridge_url
    tailer = SessionTailer(args.session_dir, args.pattern, field_map, bridge)

    if args.once:
        n = tailer.poll_once()
        print(f"[session-tail] Emitted {{n}} events")
        return 0

    tailer.run_forever()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
