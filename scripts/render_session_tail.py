_README = """\
# Session Tail — JSONL file → OTLP bridge

## What this solves

Your agent framework (OpenClaw, Mastra, custom runtime) writes per-session
JSONL files containing complete conversation data — model calls, tool results,
token usage, latency, etc. You want this data in Kibana without modifying the
agent's source code.

This script **tails** the JSONL files in real time and ships each record to the
OTLP HTTP bridge, which writes it to Elasticsearch. Zero code changes in the agent.

## Quick start

```bash
# Start the OTLP bridge first (if not running)
python generated/agent-otel-bridge.py &

# Start tailing session files
python session_tail.py \\
    --session-dir /path/to/agent/sessions \\
    --pattern "*.jsonl" \\
    --bridge-url http://localhost:{bridge_port}
```

Or via environment variables:

```bash
export SESSION_DIR=/path/to/agent/sessions
export SESSION_GLOB="*.jsonl"
export BRIDGE_URL=http://localhost:{bridge_port}
python session_tail.py
```

## Field mapping

The script maps common JSONL field names to the observability schema. Default
mapping is built in; customize by editing `field_map.json`:

```json
{{
  "model": "gen_ai.request.model",
  "input_tokens": "gen_ai.usage.input_tokens",
  "output_tokens": "gen_ai.usage.output_tokens",
  "latency_ms": "gen_ai.agent_ext.latency_ms",
  "session_id": "gen_ai.conversation.id"
}}
```

Set `FIELD_MAP_FILE=/path/to/custom_map.json` to use a custom mapping file.

## One-shot mode

To process existing files without tailing:

```bash
python session_tail.py --once --session-dir /path/to/sessions
```

## What you get in Kibana

Once the tail is running with real session data, the dashboard shows:

- **Model call latency** from actual timestamps in the JSONL
- **Token usage** per model (if the framework records `usage`)
- **Tool call distribution** (if the framework records tool names)
- **Session timeline** with events ordered by JSONL line sequence
- **Error rates** from any errors logged in the session file

## What this does NOT cover

Session JSONL files are written by the framework, so they typically don't include:

- **Reasoning trace** (why the agent chose a particular action)
- **Evaluation scores** (quality assessment of responses)
- **User feedback** (human satisfaction rating)

These require Layer 3 (active reporting). See SKILL.md for details.
"""


def render_session_tail_bundle(
    output_dir: Path,
    *,
    bridge_url: str = "http://localhost:14319",
    session_dir: str = "./sessions",
    session_glob: str = "*.jsonl",
    service_name: str = "agent-runtime",
    bridge_port: int = 14319,
) -> dict[str, Path]:
    import json as _json

    ensure_dir(output_dir)
    script_path = output_dir / "session_tail.py"
    field_map_path = output_dir / "field_map.json"
    readme_path = output_dir / "README.md"

    write_text(
        script_path,
        _SESSION_TAIL_SCRIPT.format(
            bridge_url=bridge_url,
            session_dir=session_dir,
            session_glob=session_glob,
            service_name=service_name,
        ),
    )
    write_text(
        field_map_path,
        _json.dumps(_DEFAULT_FIELD_MAP, indent=2, ensure_ascii=False) + "\n",
    )
    write_text(
        readme_path,
        _README.format(bridge_port=bridge_port),
    )
    return {
        "script": script_path,
        "field_map": field_map_path,
        "readme": readme_path,
    }


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate a session JSONL tail → OTLP bridge script")
    parser.add_argument("--output-dir", required=True, help="Target directory for the session-tail bundle")
    parser.add_argument("--bridge-url", default="http://localhost:14319", help="OTLP HTTP bridge URL")
    parser.add_argument("--session-dir", default="./sessions", help="Default directory for JSONL session files")
    parser.add_argument("--session-glob", default="*.jsonl", help="Glob pattern for session files")
    parser.add_argument("--service-name", default="agent-runtime", help="OTel service name")
    return parser.parse_args()


def main() -> int:
    try:
        args = parse_args()
        paths = render_session_tail_bundle(
            Path(args.output_dir).expanduser().resolve(),
            bridge_url=args.bridge_url,
            session_dir=args.session_dir,
            session_glob=args.session_glob,
            service_name=args.service_name,
        )
        for label, path in paths.items():
            print(f"✅ {label}: {path}")
        return 0
    except SkillError as exc:
        print_error(str(exc))
        return 1
    except Exception as exc:  # noqa: BLE001
        print_error(f"Failed to generate session tail bundle: {exc}")
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
