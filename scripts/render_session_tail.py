#!/usr/bin/env python3
"""Render a session-tail ingestion script.

Many agent frameworks (OpenClaw, Mastra, custom runtimes) write per-session
JSONL files containing the complete conversation history — model calls,
tool results, assistant responses, token usage, latency, etc.

This renderer generates a standalone Python script that:

1. ``tail -f`` the JSONL file (or glob pattern) for new lines
2. Parses each JSON line and maps fields to the observability schema
3. POSTs each event as an OTLP log to the OTLP HTTP bridge

This is **Layer 1 — zero-code injection**: the agent writes its native JSONL
as usual; the tail script runs alongside and ships data to ES. No code change
in the agent.

Bundle layout::

    session-tail/
    ├── session_tail.py      # The generated tail script
    ├── field_map.json       # Configurable field mapping
    └── README.md            # Usage guide

Usage:
    python render_session_tail.py \\
        --output-dir ./generated/session-tail \\
        --bridge-url http://localhost:14319 \\
        --session-dir /path/to/agent/sessions
"""

